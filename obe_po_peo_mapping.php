                <p>In Outcome-Based Education (OBE), the focus is on ensuring that students achieve the specified Program Outcomes (POs) and Program Educational Objectives (PEOs). The curriculum and teaching methods are designed to ensure that these outcomes and objectives are met. The alignment ensures that the students graduate with the skills and knowledge required to succeed in their professional careers and contribute meaningfully to society.</p>

                <h4>PO and PEO Alignment Process:</h4>
