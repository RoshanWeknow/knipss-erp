            <section>
                <h3 class=" text-center text-white p-1" style="background-color: #2f4f4f;">Key Characteristics of PEOs</h3>
                <p>The following are important characteristics of Program Educational Objectives:</p>
                <ul>
                    <li><strong>Long-Term Focus:</strong> PEOs typically focus on the achievements of graduates a few years after completing the program.</li>
                    <li><strong>Broad and General:</strong> PEOs are broad statements that encompass the expected accomplishments of graduates across various aspects of their careers.</li>
                    <li><strong>Aligned with Stakeholder Needs:</strong> PEOs are developed with input from stakeholders such as faculty, alumni, employers, and industry professionals to ensure they reflect current and future job market demands.</li>
                    <li><strong>Measurable:</strong> PEOs should be measurable through assessments, surveys, and feedback to determine if graduates are achieving the expected outcomes.</li>
                </ul>
            </section>

            <section>
                <h3 class=" text-center text-white p-1" style="background-color: #2f4f4f;">Examples of PEOs</h3>
