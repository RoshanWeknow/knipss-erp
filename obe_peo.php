                <ul>
                    <li><strong>PEO 1:</strong> Graduates will be able to demonstrate strong technical skills and a deep understanding of their chosen field of study.</li>
                    <li><strong>PEO 2:</strong> Graduates will develop effective communication skills and the ability to work in multidisciplinary teams.</li>
                    <li><strong>PEO 3:</strong> Graduates will be prepared to engage in lifelong learning and adapt to rapidly evolving technologies in their professional careers.</li>
                    <li><strong>PEO 4:</strong> Graduates will exhibit strong ethical standards and contribute positively to their communities and the profession.</li>
                </ul>
            </section>

            <section>
                <h3 class=" text-center text-white p-1" style="background-color: #2f4f4f;">Alignment with Program Outcomes</h3>
                <p>PEOs are aligned with Program Outcomes (POs) in OBE. While PEOs describe the broad expectations for graduates, Program Outcomes define the specific skills, knowledge, and attitudes that students should acquire during their studies. Together, PEOs and POs guide the development of the curriculum, ensuring that students are adequately prepared to meet the demands of the industry and society.</p>
            </section>

